function randomInArray(arr) {
	return arr[Math.floor(Math.random() * arr.length)];
}

export function genTiles() {
	let type = Math.floor(Math.random() * 2);
	let [block, box, point, coin, ground, player] = [
		randomInArray(TILES.block[type]),
		randomInArray(TILES.box[type]),
		randomInArray(TILES.point[type]),
		randomInArray(TILES.coin),
		randomInArray(TILES.ground),
		{
			up: TILES.player.up[type],
			down: TILES.player.down[type],
			left: TILES.player.left[type],
			right: TILES.player.right[type],
		},
	];

	return {
		block, // 2
		box, // 3  .
		point, // 4
		coin,
		ground, // 1
		player, // 5 .
	};
}

export const ObjType = Object.freeze({
	void: 0,
	block: 2, // 2
	box: 3, // 3  .
	point: 4, // 4
	// coin,  // reserved
	ground: 1, // 1
	player: 5, // 5 .
});

export function dataToTile(num, tiles) {
	let re;
	switch (num) {
		case ObjType.void:
			re = '#fff';
			break;
		case ObjType.ground:
			re = tiles.ground;
			break;
		case ObjType.block:
			re = tiles.block;
			break;
		case ObjType.box:
			re = [tiles.box, tiles.ground];
			break;
		case ObjType.point:
			re = [tiles.point, tiles.ground];
			break;
		case ObjType.player:
			re = [tiles.player.down[0], tiles.ground];
			break;
		case ObjType.point + ObjType.box:
			re = [tiles.box, tiles.point, tiles.ground];
			break;
		case ObjType.point + ObjType.player:
			re = [tiles.player, tiles.point, tiles.ground];
		default:
			re = '';
			break;
	}
	return re;
}
